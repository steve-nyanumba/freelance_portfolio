    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate
        (
            $request,
            [
            'image'=>'mimes:jpeg,jpg|max:1500'
            ]
        );
        $image = $request->file('image');
        if (isset($image)) {
            $imagename = 'background.jpg';
            if (!file_exists('img/background') ) {
                mkdir('img/background', 0777, true);
            }
            $image->move('img/background', $imagename);
        }
        return redirect()->route('admin.dashboard')->with('successMsg', 'Background Image Successfully Changed');
    }

